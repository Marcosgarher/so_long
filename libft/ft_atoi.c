int	ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	sing;

	sing = 1;
	num = 0;
	i = 0;
	while (str[i] == 32 || str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		sing = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num * sing);
}
